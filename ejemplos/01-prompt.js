let num = 0 
num = parseInt(window.prompt("Dame un número"))
window.alert("Tu numero es: " + num)


// let num = 0: Declara una variable llamada num e inicializa su valor en 0.
// window.prompt: Muestra un cuadro de diálogo modal solicitando al usuario que ingrese un número.
// parseInt(...): Convierte el valor ingresado por el usuario, normalmente una cadena de texto, en un número entero.
// window.alert: Muestra un cuadro de diálogo con un mensaje que incluye el número ingresado por el usuario, concatenado con la cadena "Tu número es:".

